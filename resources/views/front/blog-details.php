            As part of the UAE’s commitment as a member of the OECD Inclusive Framework, and in response to an assessment of the UAE’s tax framework by the European Union Code of Conduct Group on Business Taxation, the UAE issued Economic Substance Regulations (Cabinet of Ministers Resolution No. 31 of 2019), (the “Regulations”) on 30 April 2019. The Regulations require UAE onshore and free zone companies and certain other business forms that carry out any of the defined Relevant Activities to maintain and demonstrate an adequate “economic presence” in the UAE relative to the activities they undertake (“Economic Substance Test”).

            The ESR Law has been amended by the UAE Cabinet of Ministers via Cabinet Resolution no (57) dated 10 August 2020 and the updated Guidance was issues on 19 August 2020 as Ministerial Decision No. 100 of 2020.

            Some of the changes to the ESR are as follows:

            1. Definition of a Licensee
            2. Exempted licensees

            Exempted licensees have been introduced as a new category of licensee under the ESR and include, among others:

            (a) an Investment Fund;

            (b) an entity that is tax resident in a jurisdiction other than the UAE;

            (c) an entity wholly owned by UAE residents and meets the following conditions:

            (i) the entity is not part of a MNE Group;

            (ii) all of the entity’s activities are only carried out in the UAE;

            (d) a Licensee that is a branch of a foreign entity the Relevant Income of which is subject to tax in a jurisdiction other than the State.

            In order to enjoy their exempt status for ESR purposes, exempted licensees need to file a notification and provide documentary evidence demonstrating such position.

             

            3. Federal Tax Authority as main authority for ESR

            The UAE Federal Tax Authority (FTA) has now been appointed as the authority to

            (i) assess whether licensees have met the economic substance tests;

            (ii) impose administrative penalties for non-compliance; and

            (iii) hear and decide on appeals filed by licensees, among others.

             

            4. Definition of Relevant Income

            “Relevant Income” means all of that entity’s gross income from a Relevant Activity as recorded in its books and records under applicable accounting standards, whether earned in the UAE or outside the UAE and irrespective of whether the entity has derived a profit or loss from its activities.

             

            5. Definition of Distribution Business

            A Licensee is considered engaged in a “Distribution Business” if it:

            (a) Purchases raw materials or finished products from a foreign group company; and

            (b) Distributes those raw materials or finished goods.

            A Licensee is considered engaged in a “Service Centre Business” if it provides consulting, administrative or other services to a foreign group company.

             

            6. High Risk IP Licensee

            A High Risk IP Licensee is defined under Article 1 of the ESR Regulations as a Licensee which carries on an Intellectual Property Business, and meets all of the following three requirements:

            (a) The Licensee did not create the Intellectual Property Asset which it holds for the purpose of its business, and

            (b) The Licensee acquired the IP Asset from either;

            (i) A Connected Person; or

            (ii) In consideration for funding research and development by another person situated in a foreign jurisdiction; and

            (c) The Licensee licenses or has sold the IP Asset to one or more group companies, or otherwise earns separately identifiable gross income

             

            7. Branch entities

            Branches registered in the UAE are an extension of their “parent” or “head office” and do not have separate legal personality. The “parent” or “head office” registered in the UAE must file as a single Licensee, reporting the Relevant Activities of itself and all its branches in one composite Notification and / or Economic Substance Report.

            A UAE branch of a foreign entity is out of scope if the Relevant Income of the branch is subject to tax in the jurisdiction of the foreign parent / head office.

            Where a UAE entity carries on a Relevant Activity through a branch registered outside the UAE, they are not required to consolidate the activities and income of the foriegn branch for purposes of the Regulations if the Relevant Income of the foreign branch is subject to tax in the foreign jurisdiction where the branch is located.

             

            8. Submission of Notification

            The Notification must be submitted within six months from the end of the Financial Year of the Licensee or Exempted Licensee.

             

            9. Penalties

            Failure to submit a Notification: AED 20,000

            Failure to submit an Economic Substance Report: AED 50,000

            Failure to provide accurate or complete information: AED 10,000 to AED 50,000

             

            It is important to note that this amendment calls for inclusion of financial statements in the ESR Reporting. The financial statement of each licensee is to be reported.

             

